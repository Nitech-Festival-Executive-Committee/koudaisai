                    //     e.preventDefault();
                    // });
                    $('body,html').css({"overflow":"hidden","height":"100%"});
                    flag = true;
                } else {
                    // スクロール禁止 解除
                    // $(window).off('.noScroll');
                    $('body,html').css({"overflow":"visible","height":"auto"});
                    flag = false;
                }
            });
        });
